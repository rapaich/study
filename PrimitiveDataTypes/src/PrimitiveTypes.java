public class PrimitiveTypes {
    public static void main(String[] args) {

        byte first = 57, second = -17;

        System.out.println("The first variable contains value " + first);
        System.out.println("The second variable contains value " + second);
        System.out.println("Sum of variables contains value " + (first + second));
        System.out.println("Difference of variables contains value " + (first - second));
        System.out.println("Composition of variables contains value " + (first * second));
        System.out.println("Private of variables contains value " + (first / second));
    }
}
